package demo.day02;

/**
 * 运算符
 * <p>
 * 1. 算术运算符
 */
public class _5_Opreator {
    public static void main(String[] args) {
//        1.
        int x = 1;
        int y = 2;

        int z = x + y;
        System.out.println(z);

        z = x - y;
        System.out.println(z);

        z = x * y;
        System.out.println(z);

        z = x / y;
        System.out.println(z);

        z = x % y;
        System.out.println(z);
    }
}
